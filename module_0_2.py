# Задача 3: Школьная загадка
no_priority = 2 * 2 + 2  # Без приоритета (умножение выполняется перед сложением)
with_priority = 2 * (2 + 2)  # С приоритетом (в скобках сначала выполняется сложение)
comparison = no_priority == with_priority  # Сравнение двух результатов
print(no_priority)  # Ожидаемый результат: 6
print(with_priority)  # Ожидаемый результат: 8
print(comparison)  # Ожидаемый результат: False

# Объяснение кода:
# Задача 3: Школьная загадка
# Вычисляем выражение 2 * 2 + 2 без приоритета операций (умножение выполняется перед сложением).
# Затем вычисляем 2 * (2 + 2) с явным приоритетом (сначала сложение в скобках).
# Сравниваем два результата, используя оператор ==.

