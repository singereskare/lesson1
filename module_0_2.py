# Задача 4: Первый после точки
number_str = '123.456'  # Исходная строка
number_float = float(number_str)  # Преобразуем строку в дробное число
shifted = number_float * 10  # Смещаем первую цифру после запятой в целую часть
first_after_dot = int(shifted) % 10  # Берем целую часть и находим остаток от деления на 10
print(first_after_dot)  # Ожидаемый результат: 4

# Объяснение кода:
# Задача 4: Первый после точки
# Преобразуем строку '123.456' в число с плавающей запятой.
# Умножаем число на 10, чтобы "сдвинуть" первую цифру после точки в целую часть.
# Преобразуем число в целое с помощью int() и находим остаток от деления на 10, чтобы получить первую цифру после точки.
